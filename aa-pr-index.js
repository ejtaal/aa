function pr_get_img_url ( page) {
	if ( page < 1) page = 1;
	if ( page > pr.length - 1) page = pr.length - 1;
	cur_pr_page = page;
	var p = page;
	var prefixed_page = p;
	if ( p < 10) prefixed_page = "00" + p;
	else if ( p < 100) prefixed_page = "0" + p;
	return "pr/" + Math.floor(page/100) + "/pr-" + prefixed_page + ".png";
}

